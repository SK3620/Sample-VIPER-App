import Foundation

// 練習
struct Calculator {
    // テスト用足し算関数
    public func calcAdd(a: Int , b: Int)  -> Int {
        return a + b
    }
    // テスト引き算関数
    public func calcSub(a: Int , b: Int)  -> Int {
        return a - b
    }
    // テスト用割り算関数
    public func calcDiv(a: Int , b: Int)  -> Int {
        return a / b
    }
    // テスト用掛け算関数
    public func calcMulti(a: Int , b: Int)  -> Int {
        return a * b
    }
}
